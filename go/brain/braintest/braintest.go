func testListRecursive(t *testing.T, s brain.Store) {
	mustWrite(t, s, "wiki/go/interfaces.md", "i")
	mustWrite(t, s, "wiki/go/generics.md", "g")
	mustWrite(t, s, "wiki/rust/traits.md", "t")

	entries, err := s.List(ctxFor(t), "wiki", brain.ListOpts{Recursive: true})
	if err != nil {
		t.Fatalf("List: %v", err)
	}
	var got []brain.Path
	for _, e := range entries {
		if !e.IsDir {
			got = append(got, e.Path)
		}
	}
	want := []brain.Path{
		"wiki/go/generics.md",
		"wiki/go/interfaces.md",
		"wiki/rust/traits.md",
	}
	if len(got) != len(want) {
		t.Fatalf("got %v, want %v", got, want)
	}
	for i := range want {
		if got[i] != want[i] {
			t.Errorf("entry[%d] = %q, want %q", i, got[i], want[i])
		}
	}
}

