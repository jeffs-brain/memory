func testBatchCommit(t *testing.T, s brain.Store) {
	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		if err := b.Write(ctxFor(t), "memory/global/a.md", []byte("one")); err != nil {
			return err
		}
		if err := b.Write(ctxFor(t), "memory/global/b.md", []byte("two")); err != nil {
			return err
		}
		return nil
	})
	if err != nil {
		t.Fatalf("Batch: %v", err)
	}
	if string(mustRead(t, s, "memory/global/a.md")) != "one" {
		t.Error("a.md not written")
	}
	if string(mustRead(t, s, "memory/global/b.md")) != "two" {
		t.Error("b.md not written")
	}
}

func testBatchRollback(t *testing.T, s brain.Store) {
	mustWrite(t, s, "memory/global/existing.md", "original")

	sentinel := errors.New("abort")
	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		if err := b.Write(ctxFor(t), "memory/global/new.md", []byte("should not survive")); err != nil {
			return err
		}
		if err := b.Write(ctxFor(t), "memory/global/existing.md", []byte("should not overwrite")); err != nil {
			return err
		}
		return sentinel
	})
	if !errors.Is(err, sentinel) {
		t.Fatalf("expected sentinel error, got %v", err)
	}
	exists, _ := s.Exists(ctxFor(t), "memory/global/new.md")
	if exists {
		t.Error("new file survived rolled-back batch")
	}
	got := mustRead(t, s, "memory/global/existing.md")
	if string(got) != "original" {
		t.Errorf("existing file modified by rolled-back batch: got %q", got)
	}
}

func testBatchReadOwn(t *testing.T, s brain.Store) {
	mustWrite(t, s, "memory/global/base.md", "base")

	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		if err := b.Write(ctxFor(t), "memory/global/base.md", []byte("updated")); err != nil {
			return err
		}
		data, err := b.Read(ctxFor(t), "memory/global/base.md")
		if err != nil {
			return err
		}
		if string(data) != "updated" {
			t.Errorf("batch read did not see pending write: got %q", data)
		}
		return nil
	})
	if err != nil {
		t.Fatalf("Batch: %v", err)
	}
}

func testBatchWriteThenWrite(t *testing.T, s brain.Store) {
	// Within a batch, the latter Write to the same path must win. This is
	// the documented "Write followed by Write keeps the latter content"
	// contract clause.
	p := brain.Path("memory/global/overwrite.md")
	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		if err := b.Write(ctxFor(t), p, []byte("first")); err != nil {
			return err
		}
		return b.Write(ctxFor(t), p, []byte("second"))
	})
	if err != nil {
		t.Fatalf("Batch: %v", err)
	}
	got := string(mustRead(t, s, p))
	if got != "second" {
		t.Errorf("content = %q, want %q (latter Write must win)", got, "second")
	}
}

func testBatchWriteThenDelete(t *testing.T, s brain.Store) {
	// Write followed by Delete on the same path within one batch should
	// cancel both: the file must not exist after commit and no error is
	// surfaced because the batch saw the write first.
	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		p := brain.Path("memory/global/ephemeral.md")
		if err := b.Write(ctxFor(t), p, []byte("A")); err != nil {
			return err
		}
		return b.Delete(ctxFor(t), p)
	})
	if err != nil {
		t.Fatalf("Batch: %v", err)
	}
	exists, err := s.Exists(ctxFor(t), "memory/global/ephemeral.md")
	if err != nil {
		t.Fatalf("Exists: %v", err)
	}
	if exists {
		t.Fatal("Write+Delete combo left a file behind")
	}
}

func testBatchWriteThenRenameMoveContent(t *testing.T, s brain.Store) {
	// Write a pending file and rename it within the same batch. The
	// destination must carry the pending content.
	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		src := brain.Path("memory/global/pending-src.md")
		dst := brain.Path("memory/global/pending-dst.md")
		if err := b.Write(ctxFor(t), src, []byte("A")); err != nil {
			return err
		}
		return b.Rename(ctxFor(t), src, dst)
	})
	if err != nil {
		t.Fatalf("Batch: %v", err)
	}
	if srcExists, _ := s.Exists(ctxFor(t), "memory/global/pending-src.md"); srcExists {
		t.Error("source still present after Write+Rename combo")
	}
	got := string(mustRead(t, s, "memory/global/pending-dst.md"))
	if got != "A" {
		t.Errorf("destination content = %q, want %q", got, "A")
	}
}

func testBatchWriteThenRenameSamePath(t *testing.T, s brain.Store) {
	// Write to dst, then rename another path into dst. The rename should
	// win: the pending write content is replaced by the renamed source.
	mustWrite(t, s, "memory/global/renamesrc.md", "FROM-RENAME")

	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		dst := brain.Path("memory/global/renamedst.md")
		if err := b.Write(ctxFor(t), dst, []byte("FROM-WRITE")); err != nil {
			return err
		}
		return b.Rename(ctxFor(t), "memory/global/renamesrc.md", dst)
	})
	if err != nil {
		t.Fatalf("Batch: %v", err)
	}
	got := string(mustRead(t, s, "memory/global/renamedst.md"))
	if got != "FROM-RENAME" {
		t.Errorf("dst content = %q, want %q", got, "FROM-RENAME")
	}
}

func testBatchAppendThenRename(t *testing.T, s brain.Store) {
	// Append then Rename inside the same batch: the appended result must
	// land at the destination.
	mustWrite(t, s, "memory/global/log.md", "base\n")

	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		src := brain.Path("memory/global/log.md")
		dst := brain.Path("memory/global/log-rotated.md")
		if err := b.Append(ctxFor(t), src, []byte("extra\n")); err != nil {
			return err
		}
		return b.Rename(ctxFor(t), src, dst)
	})
	if err != nil {
		t.Fatalf("Batch: %v", err)
	}
	if srcExists, _ := s.Exists(ctxFor(t), "memory/global/log.md"); srcExists {
		t.Error("source still present after Append+Rename combo")
	}
	got := string(mustRead(t, s, "memory/global/log-rotated.md"))
	if got != "base\nextra\n" {
		t.Errorf("destination content = %q, want %q", got, "base\nextra\n")
	}
}

func testSubscribeAfterBatchCommit(t *testing.T, s brain.Store) {
	// Events from a batch must fire after commit, never during the user
	// callback. We assert by recording the count at each step.
	var duringCallback int
	var finalCount int
	unsub := s.Subscribe(brain.EventSinkFunc(func(evt brain.ChangeEvent) {
		finalCount++
	}))
	defer unsub()

	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		if err := b.Write(ctxFor(t), "memory/global/e1.md", []byte("one")); err != nil {
			return err
		}
		if err := b.Write(ctxFor(t), "memory/global/e2.md", []byte("two")); err != nil {
			return err
		}
		duringCallback = finalCount
		return nil
	})
	if err != nil {
		t.Fatalf("Batch: %v", err)
	}
	if duringCallback != 0 {
		t.Errorf("events fired during batch callback: %d", duringCallback)
	}
	if finalCount < 2 {
		t.Errorf("final event count = %d, want at least 2", finalCount)
	}
}

func testRootLevelFiles(t *testing.T, s brain.Store) {
	// Root-level files (kb-schema.md, schema-version.yaml) must be
	// writable, readable, and round-trip correctly through every backend.
	// This catches path-resolution gaps where a new root-level file is
	// defined in paths.go but not handled by the store's mapping layer.
	rootFiles := []struct {
		path brain.Path
		body string
	}{
		{brain.Schema(), "# KB Schema\n"},
		{brain.SchemaVersionPath(), "version: 1\n"},
	}
	for _, rf := range rootFiles {
		mustWrite(t, s, rf.path, rf.body)
		got := string(mustRead(t, s, rf.path))
		if got != rf.body {
			t.Errorf("%s: round-trip mismatch: got %q, want %q", rf.path, got, rf.body)
		}
		exists, err := s.Exists(ctxFor(t), rf.path)
		if err != nil {
			t.Fatalf("%s: Exists: %v", rf.path, err)
		}
		if !exists {
			t.Errorf("%s: Exists returned false after write", rf.path)
		}
	}
}

func testSubscribe(t *testing.T, s brain.Store) {
	var received []brain.ChangeEvent
	unsub := s.Subscribe(brain.EventSinkFunc(func(evt brain.ChangeEvent) {
		received = append(received, evt)
	}))
	defer unsub()

	mustWrite(t, s, "memory/global/foo.md", "hello")

	if len(received) != 1 {
		t.Fatalf("want 1 event, got %d", len(received))
	}
	if received[0].Path != "memory/global/foo.md" {
		t.Errorf("event path = %q, want memory/global/foo.md", received[0].Path)
	}
	if received[0].Kind != brain.ChangeCreated {
		t.Errorf("event kind = %v, want ChangeCreated", received[0].Kind)
	}
}

func testUnsubscribe(t *testing.T, s brain.Store) {
	var count int
	unsub := s.Subscribe(brain.EventSinkFunc(func(evt brain.ChangeEvent) {
		count++
	}))

	mustWrite(t, s, "memory/global/a.md", "1")
	unsub()
	mustWrite(t, s, "memory/global/b.md", "2")

	if count != 1 {
		t.Fatalf("want 1 event before unsubscribe, got %d", count)
	}
}

func testClose(t *testing.T, s brain.Store) {
	if err := s.Close(); err != nil {
		t.Fatalf("Close: %v", err)
	}
	err := s.Write(ctxFor(t), "memory/global/after.md", []byte("nope"))
	if !errors.Is(err, brain.ErrReadOnly) {
		t.Fatalf("want ErrReadOnly after Close, got %v", err)
	}
}

func testCloseIdempotent(t *testing.T, s brain.Store) {
	if err := s.Close(); err != nil {
		t.Fatalf("first Close: %v", err)
	}
	if err := s.Close(); err != nil {
		t.Fatalf("second Close: %v", err)
	}
}

func testErrReadOnlyAfterClose(t *testing.T, s brain.Store) {
	mustWrite(t, s, "memory/global/before.md", "ok")
	if err := s.Close(); err != nil {
		t.Fatalf("Close: %v", err)
	}
	ctx := ctxFor(t)
	checks := []struct {
		name string
		fn   func() error
	}{
		{"Read", func() error { _, err := s.Read(ctx, "memory/global/before.md"); return err }},
		{"Write", func() error { return s.Write(ctx, "memory/global/x.md", []byte("x")) }},
		{"Append", func() error { return s.Append(ctx, "memory/global/y.md", []byte("y")) }},
		{"Delete", func() error { return s.Delete(ctx, "memory/global/before.md") }},
		{"Rename", func() error { return s.Rename(ctx, "memory/global/before.md", "memory/global/after.md") }},
		{"Exists", func() error { _, err := s.Exists(ctx, "memory/global/before.md"); return err }},
		{"Stat", func() error { _, err := s.Stat(ctx, "memory/global/before.md"); return err }},
		{"List", func() error { _, err := s.List(ctx, "memory/global", brain.ListOpts{}); return err }},
		{"Batch", func() error {
			return s.Batch(ctx, brain.BatchOptions{}, func(b brain.Batch) error { return nil })
		}},
	}
	for _, c := range checks {
		err := c.fn()
		if !errors.Is(err, brain.ErrReadOnly) {
			t.Errorf("%s after Close: want ErrReadOnly, got %v", c.name, err)
		}
	}
}

func testListSkipsGit(t *testing.T, s brain.Store) {
	// Backends that sit on a real directory must hide the git metadata
	// tree from List results even when a recursive walk sweeps over it.
	mustWrite(t, s, "wiki/visible.md", "ok")
	localRoot, ok := s.LocalPath("wiki/visible.md")
	if !ok {
		t.Fatal("LocalPath missing for a backend that claims LocalPathAlwaysExists")
	}
	// Walk up to the brain root (parent of wiki/) and plant a .git
	// directory with a bogus file inside to prove List skips it.
	wikiDir := filepath.Dir(localRoot)
	brainRoot := filepath.Dir(wikiDir)
	gitDir := filepath.Join(brainRoot, ".git")
	if err := os.MkdirAll(filepath.Join(gitDir, "objects"), 0o755); err != nil {
		t.Fatalf("mkdir .git: %v", err)
	}
	if err := os.WriteFile(filepath.Join(gitDir, "HEAD"), []byte("ref: refs/heads/main\n"), 0o644); err != nil {
		t.Fatalf("write .git/HEAD: %v", err)
	}

	entries, err := s.List(ctxFor(t), "", brain.ListOpts{Recursive: true})
	if err != nil {
		t.Fatalf("List: %v", err)
	}
	for _, e := range entries {
		ps := string(e.Path)
		if ps == ".git" || ps == ".git/HEAD" || hasPrefix(ps, ".git/") {
			t.Errorf("List returned git metadata entry: %q", ps)
		}
	}
}

func testLocalPathSemantics(t *testing.T, s brain.Store, alwaysExists bool) {
	p := brain.Path("memory/global/local-path.md")
	mustWrite(t, s, p, "x")
	path, ok := s.LocalPath(p)
	if alwaysExists {
		if !ok {
			t.Fatalf("LocalPath(%q) returned ok=false for a filesystem-backed store", p)
		}
		if path == "" {
			t.Fatalf("LocalPath(%q) returned empty path despite ok=true", p)
		}
		if !filepath.IsAbs(path) {
			t.Errorf("LocalPath returned non-absolute path: %q", path)
		}
	} else {
		if ok {
			t.Errorf("LocalPath(%q) returned ok=true for a backend with no on-disk layout", p)
		}
		if path != "" {
			t.Errorf("LocalPath(%q) returned %q, want empty", p, path)
		}
	}
}

func testBatchRollbackEmptyRepo(t *testing.T, s brain.Store) {
	// On a brand-new git store with no commits yet, a failing batch must
	// leave the working tree empty and not create a commit.
	sentinel := errors.New("abort")
	err := s.Batch(ctxFor(t), brain.BatchOptions{Reason: "test"}, func(b brain.Batch) error {
		if err := b.Write(ctxFor(t), "memory/global/first.md", []byte("should not survive")); err != nil {
			return err
		}
		return sentinel
	})
	if !errors.Is(err, sentinel) {
		t.Fatalf("want sentinel error, got %v", err)
	}
	exists, err := s.Exists(ctxFor(t), "memory/global/first.md")
	if err != nil {
		t.Fatalf("Exists: %v", err)
	}
	if exists {
		t.Error("rolled-back file survived in empty-repo batch")
	}
}

func hasPrefix(s, prefix string) bool {
	if len(s) < len(prefix) {
		return false
	}
	return s[:len(prefix)] == prefix
}
