func testExists(t *testing.T, s brain.Store) {
	p := brain.Path("memory/global/exists.md")
	exists, err := s.Exists(ctxFor(t), p)
	if err != nil {
		t.Fatalf("Exists pre: %v", err)
	}
	if exists {
		t.Fatal("Exists true before write")
	}
	mustWrite(t, s, p, "here")
	exists, err = s.Exists(ctxFor(t), p)
	if err != nil {
		t.Fatalf("Exists post: %v", err)
	}
	if !exists {
		t.Fatal("Exists false after write")
	}
}

func testStat(t *testing.T, s brain.Store) {
	p := brain.Path("memory/global/stat.md")
	mustWrite(t, s, p, "twelve bytes")
	info, err := s.Stat(ctxFor(t), p)
	if err != nil {
		t.Fatalf("Stat: %v", err)
	}
	if info.Size != 12 {
		t.Errorf("Size = %d, want 12", info.Size)
	}
	if info.ModTime.IsZero() {
		t.Error("ModTime is zero")
	}
	if info.Path != p {
		t.Errorf("Path = %q, want %q", info.Path, p)
	}
	if info.IsDir {
		t.Error("IsDir = true for a file")
	}
}

